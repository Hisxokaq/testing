async function CmdExist(database, message, cmdname) {
    await database.query("SELECT * FROM commands WHERE serverid = ? AND name = ?", [message.guild.id, cmdname], async (error, result) => {
        if (error) return console.log(cmdname.toUpperCase() + ' : ' + error)
        if (result[0]) return
        var val = [[message.guild.id, cmdname, '0']]
        return database.query("INSERT INTO commands (serverid, name, perm) VALUES ?", [val])
    })
}

function generateQueueEmbed(message, queue, prefix) {
    let embeds = [];
    let k = 10;
  
    for (let i = 0; i < queue.length; i += 10) {
      const current = queue.slice(i, k);
      let j = i;
      k += 10;
  
      const info = current.map((track) => `**\`${++j}\`** | [\`${track.title}\`](${track.url})`).join("\n");
    
      const serverQueue =message.client.queue.get(message.guild.id);
      const embed = new MessageEmbed()
       .setAuthor("File d'attente", mus.icon)
      .setThumbnail(message.guild.iconURL())
      .setColor("YELLOW")
      .setDescription(`${info}`)
      .addField("Jouer actuellement", `[${queue[0].title}](${queue[0].url})`, true)
      .addField("Salon des notifs", serverQueue.textChannel, true)
      .addField("Salon vocal", serverQueue.voiceChannel, true)
      .setFooter("Volume actuel : "+serverQueue.volume)
       if(serverQueue.songs.length === 1)embed.setDescription(`Il n'y aura plus de musique apr√®s, \`\`${prefix}play <nom/url>\`\` pour en rajouter.`)
  
      embeds.push(embed);
    }
  
    return embeds;
   
  };
  